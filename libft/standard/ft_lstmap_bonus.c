#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*node;
	t_list	*res;
	void	*content;

	res = NULL;
	if (!f || !del)
		return (NULL);
	while (lst)
	{
		content = (*f)(lst->content);
		node = ft_lstnew(content);
		if (!node)
		{
			(*del)(content);
			ft_lstclear(&res, del);
		}
		ft_lstadd_back(&res, node);
		lst = lst->next;
	}
	return (res);
}
